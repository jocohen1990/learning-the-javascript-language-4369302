console.log("Arf");
console.log("Woof");
console.log("Meow");
console.log("Moooooooooooo");

function speak() {
  console.log("Arf");
  console.log("Woof");
  console.log("Meow");
  console.log("Moooooooooooo");
} // defined the function called speak

speak(); //this runs the function defined above

// Function expression assigned to a variable
const speak = function () {
  console.log("Arf");
  console.log("Woof");
  console.log("Meow");
  console.log("Moooooooooooo");
};

